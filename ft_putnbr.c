#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{	
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -1 * n;
		len++;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		n = n % 10;
		len++;
	}
	ft_putchar((n % 10) + '0');
	len++;
	return (len);
}
